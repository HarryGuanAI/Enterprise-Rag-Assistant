def reset_sample_dir() -> None:
    SAMPLE_DIR.mkdir(exist_ok=True)
    for path in SAMPLE_DIR.iterdir():
        if path.suffix.lower() in {".md", ".txt", ".docx", ".pdf"}:
            path.unlink()


def write_text_docs() -> None:
    for filename, content in DOCS.items():
        (SAMPLE_DIR / filename).write_text(content.strip() + "\n", encoding="utf-8")


def docx_paragraph(text: str) -> str:
    return f"<w:p><w:r><w:t>{html.escape(text)}</w:t></w:r></w:p>"


def write_docx(path: Path, title: str, paragraphs: list[str], table: list[list[str]]) -> None:
    rows = []
    for row in table:
        cells = "".join(
            f"<w:tc><w:p><w:r><w:t>{html.escape(cell)}</w:t></w:r></w:p></w:tc>" for cell in row
        )
        rows.append(f"<w:tr>{cells}</w:tr>")

    body = "\n".join(
        [
            docx_paragraph(title),
            *[docx_paragraph(paragraph) for paragraph in paragraphs],
            f"<w:tbl>{''.join(rows)}</w:tbl>",
            '<w:sectPr><w:pgSz w:w="11906" w:h="16838"/><w:pgMar w:top="1440" w:right="1440" w:bottom="1440" w:left="1440"/></w:sectPr>',
        ]
    )
    document_xml = f'''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<w:document xmlns:w="http://schemas.openxmlformats.org/wordprocessingml/2006/main">
  <w:body>{body}</w:body>
</w:document>'''

    with zipfile.ZipFile(path, "w", compression=zipfile.ZIP_DEFLATED) as archive:
        archive.writestr(
            "[Content_Types].xml",
            '''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types">
  <Default Extension="rels" ContentType="application/vnd.openxmlformats-package.relationships+xml"/>
  <Default Extension="xml" ContentType="application/xml"/>
  <Override PartName="/word/document.xml" ContentType="application/vnd.openxmlformats-officedocument.wordprocessingml.document.main+xml"/>
</Types>''',
        )
        archive.writestr(
            "_rels/.rels",
            '''<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">
  <Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument" Target="word/document.xml"/>
</Relationships>''',
        )
        archive.writestr("word/document.xml", document_xml)


def pdf_escape(text: str) -> str:
    return text.replace("\\", "\\\\").replace("(", "\\(").replace(")", "\\)")


def write_simple_pdf(path: Path, lines: list[str]) -> None:
    y = 760
    commands = ["BT", "/F1 11 Tf", "50 780 Td"]
    for index, line in enumerate(lines):
        wrapped = textwrap.wrap(line, width=92) or [""]
        for part in wrapped:
            commands.append(f"0 -16 Td ({pdf_escape(part)}) Tj")
            y -= 16
            if y < 80:
                commands.append("0 -16 Td (Continued on next policy page.) Tj")
                y = 760
        if index in {0, 1}:
            commands.append("0 -8 Td")
    commands.append("ET")
    stream = "\n".join(commands).encode("latin-1")

    objects = [
        b"<< /Type /Catalog /Pages 2 0 R >>",
        b"<< /Type /Pages /Kids [3 0 R] /Count 1 >>",
        b"<< /Type /Page /Parent 2 0 R /MediaBox [0 0 595 842] /Resources << /Font << /F1 4 0 R >> >> /Contents 5 0 R >>",
        b"<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>",
        b"<< /Length " + str(len(stream)).encode("ascii") + b" >>\nstream\n" + stream + b"\nendstream",
    ]

    output = bytearray(b"%PDF-1.4\n")
    offsets = [0]
    for number, obj in enumerate(objects, start=1):
        offsets.append(len(output))
        output.extend(f"{number} 0 obj\n".encode("ascii"))
        output.extend(obj)
        output.extend(b"\nendobj\n")
    xref_offset = len(output)
    output.extend(f"xref\n0 {len(objects) + 1}\n".encode("ascii"))
    output.extend(b"0000000000 65535 f \n")
    for offset in offsets[1:]:
        output.extend(f"{offset:010d} 00000 n \n".encode("ascii"))
    output.extend(
        f"trailer << /Size {len(objects) + 1} /Root 1 0 R >>\nstartxref\n{xref_offset}\n%%EOF\n".encode("ascii")
    )
    path.write_bytes(output)


def write_eval_dataset() -> None:
    import json

    target = ROOT / "evals" / "golden_qa.jsonl"
    lines = []
    for question, sources, sections, keywords, refusal, category in EVAL_CASES:
        lines.append(
            json.dumps(
                {
                    "question": question,
                    "expected_sources": sources,
                    "expected_sections": sections,
                    "expected_keywords": keywords,
                    "expected_refusal": refusal,
                    "category": category,
                },
                ensure_ascii=False,
            )
        )
    target.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    reset_sample_dir()
    write_text_docs()
    for filename, (title, paragraphs, table) in DOCX_DOCS.items():
        write_docx(SAMPLE_DIR / filename, title, paragraphs, table)
    for filename, lines in PDF_DOCS.items():
        write_simple_pdf(SAMPLE_DIR / filename, lines)
    write_eval_dataset()

    print(f"Generated {len(list(SAMPLE_DIR.iterdir()))} sample documents in {SAMPLE_DIR}")
    print(f"Generated {len(EVAL_CASES)} golden QA cases")


if __name__ == "__main__":
    main()
